class Solution {
public:
    int getMaxValue(vector<vector<int>>& grid) {
    	if (grid.empty())
    		return 0;
    	int x = grid.size(), y = grid[0].size();
    	vector<vector<int>> dp(x, vector<int>(y, 0));	//åˆå§‹åŒ–dpæ•°ç»„
    	dp[0][0] = grid[0][0];
    	//åˆå§‹åŒ–ç¬¬ä¸€è¡Œç¬¬ä¸€åˆ—
    	for (int i = 1; i < x; i++)
    		dp[i][0] = dp[i-1][0] + grid[i][0];
    	for (int i = 1; i < y; i++)
    		dp[0][i] = dp[0][i-1] + grid[0][i];
    	//å¼€å§‹éå†
    	for (int i = 1; i < x; i++){
    		for (int j = 1; j < y; j++){
    			dp[i][j] = max(dp[i-1][j], dp[i][j-1]) + grid[i][j];
    		}
    	}
    	return dp[x-1][y-1];

    }
};