class Solution {
public:
    int maxProductAfterCutting(int length) {
        vector<int> dp(length+1, 0);
        for (int i = 2; i <= length; i++)
        	for (int j = 1; j < i; j++)
        		dp[i] = max(dp[i], max(j*(i-j), j*(dp[i-j])));
        return dp[length];
    }
};