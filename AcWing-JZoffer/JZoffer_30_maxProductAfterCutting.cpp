class Solution {
public:
    int maxProductAfterCutting(int length) {
        if (length == 2)
        	return 1;
        else if (length == 3)
        	return 2;
        if (length % 3 == 0)
        	return (int)pow(3, length/3);
        else if (length % 3 == 1)
        	return 4*(int)pow(3, length/3-1);
        else 
        	return 2*(int)pow(3, length/3);
    }
};

